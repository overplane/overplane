package main

import (
	"bytes"
	"compress/gzip"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

func main() {
	root := flag.String("root", ".", "module root")
	out := flag.String("out", "assets_gen.go", "output file")
	flag.Parse()
	entries, err := collect(*root)
	if err != nil {
		die(err)
	}
	src, err := render(entries)
	if err != nil {
		die(err)
	}
	if err := os.WriteFile(*out, src, 0o644); err != nil {
		die(err)
	}
}

type entry struct {
	key  string
	data []byte
}

func collect(root string) ([]entry, error) {
	var paths []string
	for _, dir := range []string{"static/files"} {
		base := filepath.Join(root, dir)
		if err := filepath.WalkDir(base, func(p string, d os.DirEntry, err error) error {
			if err != nil {
				return err
			}
			if d.IsDir() {
				return nil
			}
			paths = append(paths, p)
			return nil
		}); err != nil {
			return nil, err
		}
	}
	sort.Strings(paths)
	out := make([]entry, 0, len(paths))
	for _, p := range paths {
		data, err := os.ReadFile(p)
		if err != nil {
			return nil, err
		}
		key, err := keyFor(root, p)
		if err != nil {
			return nil, err
		}
		out = append(out, entry{key: key, data: gzipBytes(data)})
	}
	return out, nil
}

func keyFor(root, p string) (string, error) {
	rel, err := filepath.Rel(root, p)
	if err != nil {
		return "", err
	}
	rel = filepath.ToSlash(rel)
	rel = strings.TrimPrefix(rel, "static/")
	return rel, nil
}

func gzipBytes(data []byte) []byte {
	var b bytes.Buffer
	zw, _ := gzip.NewWriterLevel(&b, gzip.BestCompression)
	_, _ = zw.Write(data)
	_ = zw.Close()
	return b.Bytes()
}

func render(entries []entry) ([]byte, error) {
	var b bytes.Buffer
	b.WriteString("// Code generated by internal/platform/embed/assets/cmd/gen; DO NOT EDIT.\n\n")
	b.WriteString("package assets\n\n")
	b.WriteString("var generatedAssets = map[string][]byte{\n")
	for _, e := range entries {
		fmt.Fprintf(&b, "\t%q: {", e.key)
		for i, by := range e.data {
			if i%12 == 0 {
				b.WriteString("\n\t\t")
			}
			fmt.Fprintf(&b, "0x%02x,", by)
		}
		b.WriteString("\n\t},\n")
	}
	b.WriteString("}\n")
	return format.Source(b.Bytes())
}

func die(err error) {
	fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}
